class DataTypeId
  attr_reader :type, :length, :invalid_value

  def initialize(type, length = nil, invalid_value = nil)
    @type = type
    @length = length
    @invalid_value = invalid_value
  end

  NODATA = new(0x00, 0)
  DATA8 = new(0x08, 1)
  DATA16 = new(0x09, 2)
  DATA24 = new(0x0a, 3)
  DATA32 = new(0x0b, 4)
  DATA40 = new(0x0c, 5)
  DATA48 = new(0x0d, 6)
  DATA56 = new(0x0e, 7)
  DATA64 = new(0x0f, 8)
  BOOL = new(0x10, 1, 0xff)
  MAP8 = new(0x18, 1)
  MAP16 = new(0x19, 2)
  MAP24 = new(0x1a, 3)
  MAP32 = new(0x1b, 4)
  MAP40 = new(0x1c, 5)
  MAP48 = new(0x1d, 6)
  MAP56 = new(0x1e, 7)
  MAP64 = new(0x1f, 8)
  UINT8 = new(0x20, 1, 0xff)
  UINT16 = new(0x21, 2, 0xffff)
  UINT24 = new(0x22, 3, 0xffffff)
  UINT32 = new(0x23, 4, 0xffffffff)
  UINT40 = new(0x24, 5, 0xffffffffff)
  UINT48 = new(0x25, 6, 0xffffffffffff)
  UINT56 = new(0x26, 7, 0xffffffffffffff)
  UINT64 = new(0x27, 8, 0xffffffffffffffff)
  SINT8 = new(0x28, 1, 0x80)
  SINT16 = new(0x29, 2, 0x8000)
  SINT24 = new(0x2a, 3, 0x800000)
  SINT32 = new(0x2b, 4, 0x80000000)
  SINT40 = new(0x2c, 5, 0x8000000000)
  SINT48 = new(0x2d, 6, 0x800000000000)
  SINT56 = new(0x2e, 7, 0x80000000000000)
  SINT64 = new(0x2f, 8, 0x8000000000000000)
  ENUM8 = new(0x30, 1, 0xff)
  ENUM16 = new(0x31, 2, 0xffff)
  SEMI = new(0x38, 2)
  SINGLE = new(0x39, 4)
  DOUBLE = new(0x3a, 8)
  OCTSTR = new(0x41)
  STRING = new(0x42)
  OCTSTR16 = new(0x43)
  STRING16 = new(0x44)
  ARRAY = new(0x48)
  STRUCT = new(0x4c)
  SET = new(0x50)
  BAG = new(0x51)
  TOD = new(0xe0, 4, 0xffffffff)
  DATE = new(0xe1, 4, 0xffffffff)
  UTC = new(0xe2, 4, 0xffffffff)
  CLUSTERID = new(0xe8, 2, 0xffff)
  ATTRIBUTEID = new(0xe9, 2, 0xffff)
  BACOID = new(0xea, 4, 0xffffffff)
  EUID64 = new(0xf0, 8, 0xffffffffffffffff)
  KEY128 = new(0xf1, 16)
  UNKNOWN = new(0xff)
end
